package com.github.brent.nacos.adapter.controller;

import org.springframework.http.MediaType;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.ResponseBody;
import org.springframework.web.bind.annotation.RestController;

import com.alibaba.fastjson.JSONObject;

@RestController
public class AgentController {

    @GetMapping(value = "/v1/agent/self", produces = MediaType.APPLICATION_JSON_VALUE)
    @ResponseBody
    public JSONObject getNodes() {
        JSONObject configJo = new JSONObject();
        configJo.put("Datacenter", "default");
        JSONObject jo = new JSONObject();
        jo.put("Config", configJo);
        return jo;
    }
}
